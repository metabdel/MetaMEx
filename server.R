 #=======================================================================================
 # Make all checkboxes selected by default - necessary for the select all button to work
 #=======================================================================================  
  observe({ updateCheckboxGroupInput(session, 'muscle',              choices = list_categories[['muscle_choice']],
                                     selected = if (input$bar_muscle) list_categories[['muscle_choice']])})
  observe({ updateCheckboxGroupInput(session, 'sex',                 choices = list_categories[['sex_choice']], 
                                     selected = if (input$bar_sex) list_categories[['sex_choice']])})
  observe({ updateCheckboxGroupInput(session, 'age',                 choices = list_categories[['age_choice']], 
                                     selected = if (input$bar_age) list_categories[['age_choice']])})
  observe({ updateCheckboxGroupInput(session, 'training',            choices = list_categories[['training_choice']], 
                                     selected = if (input$bar_training) list_categories[['training_choice']])})
  observe({ updateCheckboxGroupInput(session, 'obesity',             choices = list_categories[['obesity_choice']], 
                                     selected = if (input$bar_obesity) list_categories[['obesity_choice']])})
  observe({ updateCheckboxGroupInput(session, 'disease',             choices = list_categories[['disease_choice']], 
                                     selected = if (input$bar_disease) list_categories[['disease_choice']])})
  observe({ updateCheckboxGroupInput(session, 'biopsy',              choices = list_categories[['biopsy_choice']], 
                                     selected = if (input$bar_biopsy) list_categories[['biopsy_choice']])})
  observe({ updateCheckboxGroupInput(session, 'exercisetype',        choices = list_categories[['exercise_choice']], 
                                     selected = if (input$bar_exercisetype) list_categories[['exercise_choice']])})
  
  observe({ updateCheckboxGroupInput(session, 'training_duration',        choices = list_categories[['training_duration_choice']], 
