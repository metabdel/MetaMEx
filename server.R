#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#=#
#  observe({ updateCheckboxGroupInput(session, 'AA_studies', choices = list_datasets[['AA_names']],
#                                     selected = if (input$AA_all) list_datasets[['AA_names']], inline=TRUE)})
  
  AA_data <- reactive({
    tryCatch({
      #data for selected gene name
      selectedata <- AA_Stats[toupper(input$genename),]
      #Don't need to annotate studies with protocol type, remove from colum name
      colnames(selectedata) <- gsub("AcAe_", "", colnames(selectedata))
      #custom function to make a useable data frame
      selectedata <- DataForGeneName(selectedata) 
      #name columns specific for the type of exercise/inactivity
      colnames(selectedata) <- c('logFC', 'adj.P.Val', 'CI.L', 'CI.R',
                                 'Mean_Ctrl', 'Mean_Ex', 'Sd_Ctrl', 'Sd_Ex', 'size',
                                 'Studies', 'GEO', 'Exercisetype', 'Muscle', 'Sex', 'Age', 'Training', 'Obesity', 'Disease',
                                 'Biopsy', 'Duration')
      #Filter table based on selection criteria
      selectedata <- dplyr::filter(selectedata,
                                   GEO %in% input$AA_studies, 
                                   Muscle %in% input$muscle, 
                                   Sex %in% input$sex, 
                                   Age %in% input$age, 
                                   Training %in% input$training,
                                   Obesity %in% input$obesity,
                                   Disease %in% input$disease,
                                   Biopsy %in% input$biopsy,
                                   Exercisetype %in% input$exercisetype)
      #Function to make meta-analysis table (adds a row with meta-analysis score)
      selectedata <- suppressWarnings(MetaAnalysis(selectedata))
      return(selectedata)
    }, error=function(e) NULL)
  })
  
  AA_plotInput <- function() ({
    #Validate selection criteria
    validate(need(!is.null(AA_data()),   "No studies found - try different selection criteria"))
    #Plot the forest plot:
    selectedata <- AA_data()
    #make forest plot
    tabledata <- cbind(mean = c(NA , selectedata[,1]), 
                       lower= c(NA , selectedata[,3]),
                       upper= c(NA , selectedata[,4]))
    tabletext <- cbind(c(paste('Acute Aerobic Studies (', input$genename, ')', sep='') , selectedata[,10]),
                       c("logFC" , format(round(selectedata[,1], digits=2))), 
                       c("FDR"   , format(selectedata[,2],   scientific=T, digits=2)),
                       c("n" , selectedata[,9]))
    finalplot <- forestplot(tabletext, grid=T,
                            tabledata, new_page=TRUE,
                            is.summary=c(TRUE,rep(FALSE,(nrow(selectedata)-1)),TRUE),
                            xlog=F,  txt_gp = own, xlab="logFC",
                            col=fpColors(box="orange2",line="orange3", summary="orange3"),
                            boxsize=c(NA, head(selectedata$size, -1)/20, 1)) #Make the size of the symbols relative to sample size
    return(finalplot)
  })
  
  output$AA_plot <- renderPlot({ AA_plotInput() })


